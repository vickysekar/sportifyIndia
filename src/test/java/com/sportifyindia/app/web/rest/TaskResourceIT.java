    @Test
    @Transactional
    void putExistingTask() throws Exception {
        // Initialize the database
        taskRepository.saveAndFlush(task);

        int databaseSizeBeforeUpdate = taskRepository.findAll().size();
        taskSearchRepository.save(task);
        int searchDatabaseSizeBefore = IterableUtil.sizeOf(taskSearchRepository.findAll());

        // Update the task
        Task updatedTask = taskRepository.findById(task.getId()).orElseThrow();
        // Disconnect from session so that the updates on updatedTask are not directly saved in db
        em.detach(updatedTask);
        updatedTask
            .taskTitle(UPDATED_TASK_TITLE)
            .taskDescription(UPDATED_TASK_DESCRIPTION)
            .dueDate(UPDATED_DUE_DATE)
            .taskStatus(UPDATED_TASK_STATUS);
        TaskDTO taskDTO = taskMapper.toDto(updatedTask);

        restTaskMockMvc
            .perform(
                put(ENTITY_API_URL_ID, taskDTO.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(taskDTO))
            )
            .andExpect(status().isOk());

        // Validate the Task in the database
        List<Task> taskList = taskRepository.findAll();
        assertThat(taskList).hasSize(databaseSizeBeforeUpdate);
        Task testTask = taskList.get(taskList.size() - 1);
        assertThat(testTask.getTaskTitle()).isEqualTo(UPDATED_TASK_TITLE);
        assertThat(testTask.getTaskDescription()).isEqualTo(UPDATED_TASK_DESCRIPTION);
        assertThat(testTask.getDueDate()).isEqualTo(UPDATED_DUE_DATE);
        assertThat(testTask.getTaskStatus()).isEqualTo(UPDATED_TASK_STATUS);
        await()
            .atMost(5, TimeUnit.SECONDS)
            .untilAsserted(() -> {
                int searchDatabaseSizeAfter = IterableUtil.sizeOf(taskSearchRepository.findAll());
                assertThat(searchDatabaseSizeAfter).isEqualTo(searchDatabaseSizeBefore);
                List<Task> taskSearchList = IterableUtils.toList(taskSearchRepository.findAll());
                Task testTaskSearch = taskSearchList.get(searchDatabaseSizeAfter - 1);
                assertThat(testTaskSearch.getTaskTitle()).isEqualTo(UPDATED_TASK_TITLE);
                assertThat(testTaskSearch.getTaskDescription()).isEqualTo(UPDATED_TASK_DESCRIPTION);
                assertThat(testTaskSearch.getDueDate()).isEqualTo(UPDATED_DUE_DATE);
                assertThat(testTaskSearch.getTaskStatus()).isEqualTo(UPDATED_TASK_STATUS);
            });
    }

